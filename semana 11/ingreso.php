    <h1>Proceso de Admisión - Universidad Don Bosco</h1>
    
    <h2>Requisitos de Admisión:</h2>
    <ul>
        <li>Formulario de solicitud de admisión completado</li>
        <li>Examen de admisión aprobado</li>
        <li>Transcripciones académicas de la escuela secundaria</li>
        <li>Cartas de recomendación de profesores</li>
        <li>Ensayo personal</li>
        <li>Comprobante de pago de la tarifa de solicitud</li>
    </ul>

    <h2>Proceso de Solicitud:</h2>
    <ol>
        <li>Complete el formulario de solicitud en línea en el sitio web de la Universidad Don Bosco.</li>
        <li>Programa y realice el examen de admisión en el campus de la universidad o en un centro designado.</li>
        <li>Envíe las transcripciones académicas y las cartas de recomendación a la oficina de admisiones.</li>
        <li>Redacte y envíe un ensayo personal que destaque sus logros y objetivos educativos.</li>
        <li>Realice el pago de la tarifa de solicitud a través de los métodos de pago disponibles.</li>
        <li>Espera la notificación oficial de admisión por parte de la universidad.</li>
    </ol>

    <h2>Fecha Límite de Solicitud:</h2>
    <p>La fecha límite para presentar la solicitud de admisión para el próximo semestre es el 15 de julio de 2024.</p>

